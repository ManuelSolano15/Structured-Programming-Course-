/*Chapter4ex04:Write a program to print "HELLO* in big block letters;
each letter should have a height seven characters and width of five characters.*/

#include <stdio.h>

int main()
{
    printf("H   H  OOOOO  L        A \n");
    printf("H   H  O   O  L        A  \n");
    printf("H   H  O   O  L       A A     \n");
    printf("HHHHH  O   O  L      A   A  \n");
    printf("H   H  O   O  L      AAAAA \n");
    printf("H   H  O   O  L      A   A\n");
    printf("H   H  OOOOO  LLLLL  A   A\n");

    return 0;
}
